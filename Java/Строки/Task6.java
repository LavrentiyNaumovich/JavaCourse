public class Task6 {
    public static void main(String[] args) {
        boolean boolValue = true;
        char charValue = 'A';
        int intValue = 123;
        double doubleValue = 123.456;
        String strValue = "car";
        
        System.out.printf("Boolean value: %b%n", boolValue);         // Boolean value: true
        System.out.printf("Character value: %c%n", charValue);       // Character value: A
        System.out.printf("Integer value: %d%n", intValue);         // Integer value: 123
        System.out.printf("Floating point value: %.2f%n", doubleValue); // Floating point value: 123.46
        System.out.printf("String value: %s%n", strValue);           // String value: car
    }
}